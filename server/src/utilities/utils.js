export const getScaledScore = (rawScore, arr) => {
    for(let i = 0; i < arr.length; i++){
        if(arr[i][0].includes(rawScore)){
            return arr[i][1]
        }
    }
}

export const convertRawToScale = (scale, rawScore, ageMonth) => {
    if(ageMonth >= 0 && ageMonth <=3){
            return getScaledScore(rawScore, scale["0to3"])
    }
    if(ageMonth >= 4 && ageMonth <=7){
        return getScaledScore(rawScore, scale["4to7"])
    }
    return getScaledScore(rawScore, scale["8to11"])
}